def coin_toss(N, x):

 fac1 = 1
 for a in range(N, 0, -1):
     fac1 = fac1 * a
 
 fac2 = 1
 for b in range(N-x, 0, -1):
     fac2 = fac2 * b

 fac3 = 1
 for c in range(x, 0, -1):
     fac3 = fac3 * c
 
 P = fac1 / (fac2*fac3) * (0.1**x) * (0.9**(N-x))

 return round(P, 4)

coin_toss(N=10, x=1)

