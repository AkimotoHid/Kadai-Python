num_list = [5,4,3,2,1]

def bubble_sort(num_list):

 n = len(num_list)
 for a in range(n):
    for b in range(0, n - a - 1):
        if num_list[b] > num_list[b + 1]:
            num_list[b], num_list[b + 1] = num_list[b + 1], num_list[b]

 return num_list

bubble_sort(num_list)